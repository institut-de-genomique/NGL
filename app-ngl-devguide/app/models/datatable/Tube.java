package models.datatable;

public class Tube {
	public String code;
	public String volume;
	public String etat;
	public String sample;
	
	public Tube(String code, String volume, String etat, String sample) {
		super();
		this.code = code;
		this.volume = volume;
		this.etat = etat;
		this.sample = sample;
	}
}
